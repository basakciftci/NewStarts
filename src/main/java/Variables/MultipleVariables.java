package Variables;

public class MultipleVariables {
    public static void main(String[] args) {
        //apple, orange, strawberry
        int apple, orange=7, strawberry;
        apple=9;
        strawberry=15;
        System.out.println("Apple number is  "+apple);
        System.out.println("strawberry number is "+strawberry);
        System.out.println("Orange number is "+orange);
        System.out.println("Apple number is  "+apple+" strawberry number is "+strawberry+ " Orange number is "+orange);
    }
}
